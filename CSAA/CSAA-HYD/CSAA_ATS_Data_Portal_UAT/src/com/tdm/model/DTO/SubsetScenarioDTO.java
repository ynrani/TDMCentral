package com.tdm.model.DTO;

public class SubsetScenarioDTO {
	
	private String lob;
	private String scenarioDesc;
	private String noofPolicies;
	private String size;
	
	
	public String getLob() {
		return lob;
	}
	public void setLob(String lob) {
		this.lob = lob;
	}
	public String getScenarioDesc() {
		return scenarioDesc;
	}
	public void setScenarioDesc(String scenarioDesc) {
		this.scenarioDesc = scenarioDesc;
	}
	public String getNoofPolicies() {
		return noofPolicies;
	}
	public void setNoofPolicies(String noofPolicies) {
		this.noofPolicies = noofPolicies;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}

}
